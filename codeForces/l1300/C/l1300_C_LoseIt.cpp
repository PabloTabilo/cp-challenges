void solve(){
    int n;
    cin>>n;
    vector<int> a(n,0);
    map<int,queue<int>> m;
    for(int i=0;i<n;i++) cin>>a[i];
    int res=0;
    int cs=0;
    int l=1;
    for(int i=n-1;i>=0;i--){
        if(a[i]==42){
            m[42].push(1);
        }else if(a[i]==23){
            if(!m[42].empty()){
                l=m[42].front();
                m[23].push(l+1);
                m[42].pop();
            }else res++;
        }else if(a[i]==16){
            if(!m[23].empty()){
                l=m[23].front();
                m[16].push(l+1);
                m[23].pop();
            }else res++;
        }else if(a[i]==15){
            if(!m[16].empty()){
                l=m[16].front();
                m[15].push(l+1);
                m[16].pop();
            }else res++;
        }else if(a[i]==8){
            if(!m[15].empty()){
                l=m[15].front();
                m[8].push(l+1);
                m[15].pop();
            }else res++;
        }else if(a[i]==4){
            if(!m[8].empty()){
                m[8].pop();
                cs+=6;
            }else res++;
        }
    }
    res+=m[42].size() + m[23].size() + m[16].size() + m[15].size() + m[8].size();
    cout<<n-cs<<endl;
}

