int st(int su1, int su2, int deep){
    if(deep<=0){
        if(su1==su2) return 1;
        return 0;
    }
    return st(su1,su2-1,deep-1) + st(su1,su2+1,deep-1);
}

void solve(){
    string s1, s2;
    getline(cin,s1);
    getline(cin,s2);
    int su1=0,su2=0, deep=0;
    int n=s1.length();
    for(int i=0;i<n;i++){
        if(s1[i]=='+') su1++;
        else if(s1[i]=='-') su1--;
        if(s2[i]=='+') su2++;
        else if(s2[i]=='-') su2--;
        else if(s2[i]=='?') deep++;
    }
    double ans=1.0;
    if(deep>0){
       double tot=1<<deep; 
       int res = st(su1,su2,deep);
       ans = (double)res/tot;
    }
    if(deep<=0 && su1!=su2) ans=.0;
    cout<<setprecision(13)<<fixed<<ans<<endl;
}

